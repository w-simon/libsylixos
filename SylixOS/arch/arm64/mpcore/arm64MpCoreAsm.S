#ifndef ASSEMBLY
#define ASSEMBLY 1
#endif

#include <arch/assembler.h>

#if LW_CFG_SMP_EN > 0

    FILE_BEGIN()
    
    IMPORT_LABEL(bspMpInt)
    
    EXPORT_LABEL(archMpCur)
    EXPORT_LABEL(archMpInt)
    
    WEAK(archMpCur)
    WEAK(archMpInt)

;/*********************************************************************************************************
;  获取当前核 ID
;*********************************************************************************************************/

FUNC_DEF(archMpCur)
    MRS     X1 , MPIDR_EL1                                              ;/*  read mpidr reg              */
    AND     X0 , X1 , #0xf
    RET
    FUNC_END()
    
;/*********************************************************************************************************
;  产生一次核间中断
;*********************************************************************************************************/

FUNC_DEF(archMpInt)
    B       bspMpInt
    FUNC_END()
    
    FILE_END()

#endif
