#ifndef ASSEMBLY
#define ASSEMBLY 1
#endif

#include <arch/assembler.h>

    FILE_BEGIN()

    EXPORT_LABEL(ppcGetMSR)
    EXPORT_LABEL(ppcGetDAR)
    EXPORT_LABEL(ppcSetDEC)
    EXPORT_LABEL(ppcGetDEC)

;/*********************************************************************************************************
;  读取 MSR 寄存器
;*********************************************************************************************************/

FUNC_DEF(ppcGetMSR)
    MFMSR   R3
    ISYNC
    BLR
    FUNC_END()

;/*********************************************************************************************************
;  读取 DAR 寄存器
;*********************************************************************************************************/

FUNC_DEF(ppcGetDAR)
    MFSPR   R3 , DAR
    ISYNC
    BLR
    FUNC_END()

;/*********************************************************************************************************
;  读取和设置 DEC 寄存器
;*********************************************************************************************************/

FUNC_DEF(ppcSetDEC)
    MTSPR   DEC , R3
    BLR
    FUNC_END()

FUNC_DEF(ppcGetDEC)
    MFSPR   R3 , DEC
    BLR
    FUNC_END()

    FILE_END()

